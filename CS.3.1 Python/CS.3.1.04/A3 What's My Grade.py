mark = int(input("Enter your exam marks (0-100): "))

if mark >= 91:
    print("You got an A*")
elif mark >= 75:
    print("You got an A")
elif mark >= 60:
    print("You got an B")
elif mark >= 50:
    print("You got an C")
elif mark >= 35:
    print("You got an D")
elif mark >= 20:
    print("You got an E")
else:
    print("You got an U")



